"use client";
import Link from "next/link";
import { usePathname } from "next/navigation";

const NAV_ITEMS = [
  { href: "/home", label: "ホーム", icon: HomeIcon },
  { href: "/foods", label: "食材", icon: FoodIcon },
  { href: "/plan", label: "献立", icon: PlanIcon },
  { href: "/settings", label: "設定", icon: SettingIcon },
] as const;

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-1/2 z-50 flex w-full max-w-mobile -translate-x-1/2 items-stretch border-t border-brand-bg bg-white shadow-[0_-4px_20px_rgba(0,0,0,0.06)]">
      {NAV_ITEMS.map(({ href, label, icon: Icon }) => {
        const active = pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className="flex flex-1 select-none flex-col items-center justify-center gap-0.5 py-2 transition-colors duration-150"
          >
            <div
              className={`p-1.5 rounded-lg transition-colors ${
                active ? "bg-brand-bg" : ""
              }`}
            >
              <Icon
                className={`w-5 h-5 transition-colors ${
                  active ? "text-brand" : "text-gray-400"
                }`}
              />
            </div>
            <span
              className={`text-[10px] font-medium transition-colors ${
                active ? "text-brand font-bold" : "text-gray-400"
              }`}
            >
              {label}
            </span>
            {active && (
              <span className="absolute bottom-1 h-1 w-1 rounded-full bg-brand-light" />
            )}
          </Link>
        );
      })}
    </nav>
  );
}

